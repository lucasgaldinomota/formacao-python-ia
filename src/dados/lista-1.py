# Lista é uma estrutura indexada
# Índices  0  1  2  3
numeros = [1, 2, 3, 4]
print(numeros)

frutas = ["maça", "banana", "laranja"]
print(frutas)

misto = [1, True, 3.14, "João", [1, 2, 3]]
print(misto)

# Acessar os elementos pelo índice
print(numeros[3])
print(frutas[1])
print(misto[4][1])
print(frutas[-2])
